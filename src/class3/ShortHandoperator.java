package class3;

public class ShortHandoperator {
	public static void main(String[] args) {

		int num1 = 10;
		int num2 = 40;
		num1 = num1 + 10;// 20
		num1 += 10;
		System.out.println(num1);// 30

		num1 -= 10;
		System.out.println(num1);// 20

		num2 /= 10;
		System.out.println(num2);// 4

		// Declare a variable and increase by 100 using ShortHand Operator

		int number = 0;
		number += 100;
		System.out.println(number);
		// Declare a variable and decrease by 67 using ShortHand Operator
		int number1 = 0;
		number1 -= 67;
		System.out.println(number1);

		System.out.println("=============================================");

		
		

	}

}
