package class3;

public class NamingConvention {
	public static void main(String[] args) {
		
		
		// we never use java keyword for naming
		
		// int public=2;
		// when we name the variable, we should not have space between two words.
		
		// String MyLast Name= "KHAN";
		
		// we use camel casing for name the variable
		
		String numberButItIsString = "10";
		int num = 5;
		System.out.println(numberButItIsString+num);
		
		int num1 = 2;
		int num2 = 4;
		
		System.out.println(num1*num2);
		
		// Addition sign Is +
		System.out.println(2+4);
	}

}
