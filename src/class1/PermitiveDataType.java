package class1;

public class PermitiveDataType {
	
	public static void main(String[] args) {
		
		// byte
		
		byte box1 = 5;// -128 till 127
		
		short box2 = 55;//-32768 till 32767
		
   	    int box3 = 1200;//-2147483648 till 2147483647
   	    
   	    long box4 = 1256364777484L;//If number is more then 10 digit need to add L
   	    
   	    float box5 = 10.647363488734646f;// only it will show six digit after decimal
   	    
   	    double box6 = 98.3787864836837286842;//It will show ten digit after decimal
   	 
   	    char a = 'A';//It will take only one character 
   	    char var ='*';
   	    
   	    boolean isRain= true;// boolean will take true or false value 
   	    boolean isSnow= false;
   	    
   	    System.out.println(box1);
   	    System.out.println(box2);
   	    System.out.println(box3);
   	    System.out.println(box4);
   	    System.out.println(box5);
   	    System.out.println(box6);
   	    System.out.println(a);
   	    System.out.println(var);
   	    System.out.println(isRain);
   	    System.out.println(isSnow);
                              
  }
}
