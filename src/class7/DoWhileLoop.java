package class7;

public class DoWhileLoop {
	public static void main(String[] args) {

		int num1 = 1;

		// We want to have a program to print number 1-10

		do {
			System.out.print(num1 + " ");
			num1++;
		} while (num1 <= 10);
		System.out.println();
		int num2 = 10;
		while (num2 >= 1) {
			System.out.print(num2 + " ");
			num2--;

		}

	}

}
